package co.com.ies.bingo.mongotest.bingo75.bingo.bingo75.bingo;

public class BaloteraException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7880707337973898328L;

	/**
	 * 
	 */
	public BaloteraException() {
		super();
		
	}

	/**
	 * @param message
	 * @param cause
	 * @param enableSuppression
	 * @param writableStackTrace
	 */
	public BaloteraException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	/**
	 * @param message
	 * @param cause
	 */
	public BaloteraException(String message, Throwable cause) {
		super(message, cause);
		
	}

	/**
	 * @param message
	 */
	public BaloteraException(String message) {
		super(message);
		
	}

	/**
	 * @param cause
	 */
	public BaloteraException(Throwable cause) {
		super(cause);
		
	}
	
	
	

}
